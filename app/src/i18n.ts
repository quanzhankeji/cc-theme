export function localizeRuntimeText(value: string, locale: ManagerLocale): string {
  if (locale === "zh-CN") return value;
  const exact = englishRuntimeText.get(value);
  if (exact) return exact;
  const themeCount = value.match(/^(\d+) 个主题家族可用$/u);
  if (themeCount) return `${themeCount[1]} theme families available`;
  return value
    .replace(/^已验证 · ([A-Z0-9]+)$/u, "Verified · $1")
    .replace(/^(.+) 客户端$/u, "$1 client")
    .replace(/^未找到官方 (.+)$/u, "Official $1 not found")
    .replace(/^诊断完成：(\d+) 项失败，(\d+) 项需要注意$/u, "Diagnostics completed: $1 failed, $2 need attention")
    .replace(/^适配器诊断结果：([A-Za-z0-9._-]+)$/u, "Adapter diagnostics: $1")
    .replace(/^运行中 · 端口 (\d+) · 始终适配最新版$/u, "Running · Port $1 · Latest version policy")
    .replace(/^已就绪 · 端口 (\d+) · 启动时探测运行时$/u, "Ready · Port $1 · Runtime checked at launch")
    .replace(/^已验证 · 端口 (\d+) · 等待启动$/u, "Verified · Port $1 · Waiting to launch");
}
