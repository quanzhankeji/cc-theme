const translations: Record<ManagerLocale, Record<TranslationKey, string>> = {
  "zh-CN": zhCN,
  "en-US": enUS,
};

export type Translate = (key: TranslationKey, variables?: Record<string, string | number>) => string;

export function createTranslator(locale: ManagerLocale): Translate {
  return (key, variables = {}) => Object.entries(variables).reduce(
    (message, [name, value]) => message.replaceAll(`{${name}}`, String(value)),
    translations[locale][key],
  );
}

export function detectManagerLocale(): ManagerLocale {
  try {
    const saved = window.localStorage.getItem(LOCALE_STORAGE_KEY);
    if (saved === "zh-CN" || saved === "en-US") return saved;
  } catch {
    // Continue with the operating-system language when storage is unavailable.
  }
  const languages = navigator.languages?.length ? navigator.languages : [navigator.language];
  return languages.some((language) => language.toLowerCase().startsWith("zh")) ? "zh-CN" : "en-US";
}

